

def regresar_tupla(nombre: str = '', apellido: str = '', edad: int = None) ->():
    """
     Retorna los datos de una persona en una tupla (), convirtiendo los datos ingresados a mayusculas.

    :param nombre: str = nombre de la persona.
    :param apellido: str = apellido de la persona.
    :param edad: int = edad de la persona.

    :return: (NOMBRE, APELLIDO, EDAD)
    """
    pass
    return nombre.upper(), apellido.upper(), edad
